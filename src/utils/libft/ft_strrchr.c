#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	if (s == NULL)
		return (NULL);
	len = ft_strlen(s);
	while (len >= 0 && s[len] != c)
	{
		if (s[len] == (char)c)
			return ((char *)&s[len]);
		len--;
	}
	if (s[len] == (char)c)
		return ((char *)&s[len]);
	return (NULL);
}
