#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*p;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		p = (*lst)->next;
		del ((*lst)->content);
		gb_free(*lst);
		*lst = NULL;
		(*lst) = p;
	}
}
